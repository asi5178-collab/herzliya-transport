# в”Ђв”Ђ detect figure numbers from inline marker lines в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Matches: *(Figure 4 вЂ” ... вЂ” appears below)* or *(Figure 9 ... | Figure 10 ... вЂ” appear below)*
MARKER_RE = re.compile(r'^\*\(Figure[s]?\s+([\d ,|]+)', re.IGNORECASE)
FIG_NUM_RE = re.compile(r'\b(\d+)\b')


def extract_fig_numbers(marker_line: str):
    """Return list of integer figure numbers from a marker line."""
    nums = FIG_NUM_RE.findall(marker_line)
    return [int(n) for n in nums if int(n) in FIGURE_INFO]


# в”Ђв”Ђ helpers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
def set_style(doc):
    """Apply clean academic styling."""
    # Body text
    normal = doc.styles['Normal']
    normal.font.name = 'Times New Roman'
    normal.font.size = Pt(12)
    normal.paragraph_format.space_after = Pt(6)
    normal.paragraph_format.line_spacing = Pt(18)

    # Headings
    for lvl, sz in [(1, 16), (2, 14), (3, 12)]:
        h = doc.styles[f'Heading {lvl}']
        h.font.name = 'Times New Roman'
        h.font.size = Pt(sz)
        h.font.bold = True
        h.font.color.rgb = RGBColor(0x00, 0x00, 0x00)
        h.paragraph_format.space_before = Pt(12)
        h.paragraph_format.space_after  = Pt(6)

    # Caption style (create if absent)
    if 'Caption' not in [s.name for s in doc.styles]:
        cap = doc.styles.add_style('Caption', WD_STYLE_TYPE.PARAGRAPH)
    else:
        cap = doc.styles['Caption']
    cap.font.name  = 'Times New Roman'
    cap.font.size  = Pt(10)
    cap.font.italic = True
    cap.font.color.rgb = RGBColor(0x33, 0x33, 0x33)
    cap.paragraph_format.alignment   = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.space_before = Pt(4)
    cap.paragraph_format.space_after  = Pt(10)


def insert_figure(doc, fig_num: int, width_inches=5.8):
    """Insert figure image + caption paragraph into doc."""
    info = FIGURE_INFO.get(fig_num)
    if not info:
        return
    fname, caption = info
    fpath = FIGURES_DIR / fname
    if not fpath.exists():
        print(f"  вљ пёЏ  Missing figure file: {fpath}")
        return

    # separator line before figure
    sep = doc.add_paragraph()
    sep.paragraph_format.space_before = Pt(6)
    sep.paragraph_format.space_after  = Pt(2)

    # image paragraph
    img_para = doc.add_paragraph()
    img_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    img_para.paragraph_format.space_before = Pt(4)
    img_para.paragraph_format.space_after  = Pt(2)
    run = img_para.add_run()
    run.add_picture(str(fpath), width=Inches(width_inches))

    # caption paragraph
    cap_para = doc.add_paragraph(style='Caption')
    cap_para.text = caption
    cap_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    print(f"  вњ“ Figure {fig_num}: {fname}")


def apply_inline_formatting(para, text: str):
    """Add a run with basic **bold** and *italic* markdown rendering."""
    # Split by **bold** and *italic*
    pattern = re.compile(r'\*\*(.+?)\*\*|\*(.+?)\*|(`[^`]+`)')
    pos = 0
    for m in pattern.finditer(text):
        # plain text before match
        if m.start() > pos:
            para.add_run(text[pos:m.start()])
        if m.group(1):       # **bold**
            run = para.add_run(m.group(1))
            run.bold = True
        elif m.group(2):     # *italic*
            run = para.add_run(m.group(2))
            run.italic = True
        elif m.group(3):     # `code`
            run = para.add_run(m.group(3)[1:-1])
            run.font.name = 'Courier New'
        pos = m.end()
    if pos < len(text):
        para.add_run(text[pos:])


# в”Ђв”Ђ main converter в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
def convert():
    doc = Document()
    set_style(doc)

    lines = PAPER_MD.read_text(encoding='utf-8').splitlines()

    # State flags
    in_figure_index = False    # suppress image insertion in Figure Index section
    in_references   = False
    pending_figures = []       # figures queued for insertion after next real paragraph

    def flush_pending():
        for fn in pending_figures:
            insert_figure(doc, fn)
        pending_figures.clear()

    i = 0
    while i < len(lines):
        raw = lines[i]
        line = raw.strip()
        i += 1

        # в”Ђв”Ђ horizontal rule в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        if line in ('---', '***', '___'):
            flush_pending()
            p = doc.add_paragraph()
            pPr = p._p.get_or_add_pPr()
            pBdr = OxmlElement('w:pBdr')
            bottom = OxmlElement('w:bottom')
            bottom.set(qn('w:val'), 'single')
            bottom.set(qn('w:sz'), '4')
            bottom.set(qn('w:space'), '1')
            bottom.set(qn('w:color'), 'AAAAAA')
            pBdr.append(bottom)
            pPr.append(pBdr)
            p.paragraph_format.space_after = Pt(6)
            continue

        # в”Ђв”Ђ headings в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        if line.startswith('# ') and not line.startswith('## '):
            flush_pending()
            p = doc.add_heading(line[2:].strip(), level=1)
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            continue

        if line.startswith('## '):
            flush_pending()
            heading_text = line[3:].strip()
            if heading_text == 'Figure Index':
                in_figure_index = True
            elif heading_text == 'References':
                in_figure_index = False
                in_references = True
            else:
                in_figure_index = False
                in_references = False
            doc.add_heading(heading_text, level=2)
            continue

        if line.startswith('### '):
            flush_pending()
            doc.add_heading(line[4:].strip(), level=3)
            continue

        # в”Ђв”Ђ blank line в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        if not line:
            continue

        # в”Ђв”Ђ inline figure marker lines в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        # Pattern: *(Figure N вЂ” ... вЂ” appears below)* or *(Figure N ... | Figure M ...)*
        if line.startswith('*(Figure') and ('appears below' in line or 'appear below' in line):
            fig_nums = extract_fig_numbers(line)
            if fig_nums:
                pending_figures.extend(fig_nums)
            # Don't output the marker as text вЂ” just queue the figures
            continue

        # в”Ђв”Ђ Figure Index bullets вЂ” keep as text, no images в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        if in_figure_index and line.startswith('- **Figure'):
            # Render as a plain reference list item (text only)
            p = doc.add_paragraph(style='Normal')
            p.paragraph_format.left_indent = Pt(18)
            p.paragraph_format.space_after  = Pt(3)
            clean = re.sub(r'\*\*(.+?)\*\*', r'\1', line.lstrip('- '))
            p.add_run(clean)
            continue

        # в”Ђв”Ђ metadata header lines (bold: value) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        if line.startswith('**') and ':' in line and line.endswith('**') is False:
            # e.g. **Author:** Levi Eliasaf
            flush_pending()
            p = doc.add_paragraph(style='Normal')
            p.paragraph_format.space_after = Pt(2)
            apply_inline_formatting(p, line)
            continue

        # в”Ђв”Ђ regular paragraph в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
        flush_pending()

        p = doc.add_paragraph(style='Normal')

        # First-line indent for body paragraphs (not after headings handled by style)
        if not in_figure_index and not in_references:
            p.paragraph_format.first_line_indent = Pt(18)

        apply_inline_formatting(p, line)

    # Flush any remaining pending figures at end of document
    flush_pending()

    doc.save(str(OUTPUT_DOCX))
    size_mb = OUTPUT_DOCX.stat().st_size / (1024 * 1024)
    print(f"\nвњ…  Saved: {OUTPUT_DOCX}")
    print(f"   Size : {size_mb:.2f} MB")


if __name__ == '__main__':
    convert()
